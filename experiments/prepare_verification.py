#!/usr/bin/env python3
"""
Prepare verification inputs from phase1_results.json.

Reads the exploit patches generated by the Hackability Attacker and writes
individual JSONL prediction files for each high-confidence exploit, formatted
for the swebench.harness.run_evaluation module.

Output: experiments/verify_inputs/<instance_id>_exploit_<N>.jsonl
Each file contains one line: {"instance_id": ..., "model_name_or_path": ..., "model_patch": ...}
"""

import json
import os
from pathlib import Path


def main():
    results_path = Path(__file__).parent / "phase1_results.json"
    output_dir = Path(__file__).parent / "verify_inputs"
    output_dir.mkdir(exist_ok=True)

    if not results_path.exists():
        print("ERROR: experiments/phase1_results.json not found.")
        print("Run: python experiments/run_phase1.py --n-tasks 50")
        return

    with open(results_path) as f:
        results = json.load(f)

    total_exploits = 0
    total_tasks = 0
    skipped_empty = 0

    for task_result in results:
        instance_id = task_result["instance_id"]
        strategies = task_result.get("strategies", [])
        patches = task_result.get("exploit_patches", [])

        # Match strategies to patches (they're in the same order)
        high_conf_indices = []
        for i, s in enumerate(strategies):
            if s.get("confidence", 0) >= 7:
                high_conf_indices.append(i)

        if not high_conf_indices:
            continue

        total_tasks += 1

        for rank, strat_idx in enumerate(high_conf_indices[:3]):  # Top 3
            # Get the corresponding patch
            if strat_idx < len(patches) and patches[strat_idx]:
                patch = patches[strat_idx]
            else:
                skipped_empty += 1
                continue

            # Validate patch is non-trivial
            patch = patch.strip()
            if len(patch) < 10 or "---" not in patch:
                skipped_empty += 1
                continue

            pred = {
                "instance_id": instance_id,
                "model_name_or_path": f"envaudit-exploit-{rank}",
                "model_patch": patch,
            }

            safe_id = instance_id.replace("/", "__")
            filename = f"{safe_id}_exploit_{rank}.jsonl"
            filepath = output_dir / filename

            with open(filepath, "w") as f:
                f.write(json.dumps(pred) + "\n")

            total_exploits += 1

    print(f"Prepared {total_exploits} exploit predictions from {total_tasks} tasks")
    print(f"Skipped {skipped_empty} exploits (empty or invalid patches)")
    print(f"Output directory: {output_dir}")

    # Write a manifest for the workflow
    manifest = {
        "total_exploits": total_exploits,
        "total_tasks": total_tasks,
        "skipped_empty": skipped_empty,
    }
    with open(output_dir / "manifest.json", "w") as f:
        json.dump(manifest, f, indent=2)


if __name__ == "__main__":
    main()
